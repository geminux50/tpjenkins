package fr.exemple.hello;

public class Operation {

	public Operation() {

	}

	public int add(int one, int two) {
		return one + two;
	}
	
	public int sub(int one, int two) {
		return one - two;
	}
	
	public int mul(int one, int two) {
		return one * two;
	}
	
	public int div(int one, int two) {
		return one / two;
	}
}
